/// Nebula UI breakpoint definitions for responsive layouts.
///
/// These values define the boundaries between device categories
/// used throughout the design system.
abstract final class NebulaBreakpoints {
  /// Maximum width considered "mobile" (< 600).
  static const double mobile = 600;

  /// Maximum width considered "tablet" (< 1024).
  static const double tablet = 1024;

  /// Minimum width considered "desktop" (>= 1024).
  static const double desktop = 1024;
}
